// processPacketsConsolidated consolidates fast packets into single lines with extended data
// while preserving the chronological order of all messages
func (r *rawFmt) processPacketsConsolidated() {
	type fastKey struct {
		pgn         uint32
		source      uint8
		destination uint8
		seqId       uint8
	}

	// Track ongoing fast packet sequences
	activeSequences := make(map[fastKey][]packet)

	for _, pkt := range r.packets {
		// Check if this is a fast packet PGN
		if pgn.IsFast(pkt.pgn) && len(pkt.frame.Data) >= 1 {
			packetSeqId := (pkt.frame.Data[0] >> 5) & 0x07
			frameNum := pkt.frame.Data[0] & 0x1F

			key := fastKey{
				pgn:         pkt.pgn,
				source:      pkt.source,
				destination: pkt.destination,
				seqId:       packetSeqId,
			}

			if frameNum == 0 {
				// Start of a new fast packet sequence
				activeSequences[key] = []packet{pkt}
			} else if existing, exists := activeSequences[key]; exists {
				// Continuation of an existing sequence
				activeSequences[key] = append(existing, pkt)

				// Check if this might be the last frame
				// We can't know for sure without the total length from frame 0,
				// but we can make a reasonable guess based on data patterns
				if r.isSequenceComplete(activeSequences[key]) {
					// Consolidate and output the complete sequence
					r.outputConsolidatedSequence(activeSequences[key])
					delete(activeSequences, key)
				}
			} else {
				// Continuation frame without a start frame - output as single frame
				r.outputSinglePacket(pkt)
			}
		} else {
			// Single frame packet - output immediately
			r.outputSinglePacket(pkt)
		}
	}

	// Output any remaining incomplete sequences as individual frames
	for _, sequence := range activeSequences {
		for _, pkt := range sequence {
			r.outputSinglePacket(pkt)
		}
	}
}

// isSequenceComplete checks if a fast packet sequence is complete
func (r *rawFmt) isSequenceComplete(sequence []packet) bool {
	if len(sequence) == 0 {
		return false
	}

	// Must start with frame 0
	firstFrame := sequence[0]
	if len(firstFrame.frame.Data) < 2 || (firstFrame.frame.Data[0]&0x1F) != 0 {
		return false
	}

	totalLength := int(firstFrame.frame.Data[1])
	if totalLength == 0 || totalLength > 223 {
		return false
	}

	// Calculate expected number of frames
	// First frame has 6 bytes of data, subsequent frames have 7 bytes each
	dataInFirstFrame := 6
	remainingData := totalLength - dataInFirstFrame
	if remainingData <= 0 {
		return len(sequence) == 1
	}

	additionalFrames := (remainingData + 6) / 7 // Round up
	expectedFrames := 1 + additionalFrames

	return len(sequence) >= expectedFrames
}

// outputSinglePacket outputs a single packet in standard format
func (r *rawFmt) outputSinglePacket(pkt packet) {
	line := fmt.Sprintf("%s,%d,%d,%d,%d,%d,%02x,%02x,%02x,%02x,%02x,%02x,%02x,%02x\n",
		pkt.time.Format("2006-01-02T15:04:05Z"), pkt.priority, pkt.pgn, pkt.source,
		pkt.destination, pkt.frame.Length, pkt.frame.Data[0], pkt.frame.Data[1],
		pkt.frame.Data[2], pkt.frame.Data[3], pkt.frame.Data[4], pkt.frame.Data[5],
		pkt.frame.Data[6], pkt.frame.Data[7])
	r.contents = append(r.contents, line...)
}

// outputConsolidatedSequence outputs a consolidated fast packet sequence
func (r *rawFmt) outputConsolidatedSequence(sequence []packet) {
	// Sort by frame number for proper reconstruction
	for i := 0; i < len(sequence)-1; i++ {
		for j := 0; j < len(sequence)-1-i; j++ {
			frameNumJ := sequence[j].frame.Data[0] & 0x1F
			frameNumJ1 := sequence[j+1].frame.Data[0] & 0x1F
			if frameNumJ > frameNumJ1 {
				sequence[j], sequence[j+1] = sequence[j+1], sequence[j]
			}
		}
	}

	consolidatedData, totalLength := r.consolidateFastGroup(sequence)
	if len(consolidatedData) > 0 {
		firstPacket := sequence[0]
		line := fmt.Sprintf("%s,%d,%d,%d,%d,%d", firstPacket.time.Format("2006-01-02T15:04:05Z"),
			firstPacket.priority, firstPacket.pgn, firstPacket.source, firstPacket.destination, totalLength)
		for _, b := range consolidatedData {
			line += fmt.Sprintf(",%02x", b)
		}
		line += "\n"
		r.contents = append(r.contents, line...)
	}
}

// consolidateFastGroup reconstructs the original data from a fast packet sequence
func (r *rawFmt) consolidateFastGroup(packetGroup []packet) ([]uint8, int) {
	if len(packetGroup) == 0 {
		return nil, 0
	}

	// First frame should have the total length in Data[1]
	firstFrame := packetGroup[0]
	if len(firstFrame.frame.Data) < 2 {
		return nil, 0
	}

	frameNum := firstFrame.frame.Data[0] & 0x1F
	if frameNum != 0 {
		// Not a proper fast packet start
		return nil, 0
	}

	totalLength := int(firstFrame.frame.Data[1])
	if totalLength == 0 || totalLength > 223 {
		// Invalid length for fast packet
		return nil, 0
	}

	consolidatedData := make([]uint8, 0, totalLength)

	// Extract data from first frame (6 bytes after seq/length bytes)
	for i := 2; i < 8 && len(consolidatedData) < totalLength; i++ {
		consolidatedData = append(consolidatedData, firstFrame.frame.Data[i])
	}

	// Extract data from continuation frames (7 bytes each after seq byte)
	for frameIdx := 1; frameIdx < len(packetGroup) && len(consolidatedData) < totalLength; frameIdx++ {
		currentFrame := packetGroup[frameIdx]
		if len(currentFrame.frame.Data) < 1 {
			continue
		}

		expectedFrameNum := uint8(frameIdx)
		actualFrameNum := currentFrame.frame.Data[0] & 0x1F
		if actualFrameNum != expectedFrameNum {
			// Frame sequence error - skip this group
			return nil, 0
		}

		// Extract up to 7 bytes of data
		for i := 1; i < 8 && len(consolidatedData) < totalLength; i++ {
			consolidatedData = append(consolidatedData, currentFrame.frame.Data[i])
		}
	}

	// Trim to exact length
	if len(consolidatedData) > totalLength {
		consolidatedData = consolidatedData[:totalLength]
	}

	return consolidatedData, totalLength
}

